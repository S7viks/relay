#!/usr/bin/env node
/**
 * Writes web/js/gaiol-api-config.js from GAIOL_API_PUBLIC_URL (or NEXT_PUBLIC_GAIOL_API_URL).
 * Used by Vercel build so the static frontend can call a hosted Go API.
 */
const fs = require('fs');
const path = require('path');

const raw = (process.env.GAIOL_API_PUBLIC_URL || process.env.NEXT_PUBLIC_GAIOL_API_URL || '').trim();
const base = raw.replace(/\/+$/, '');

const onVercel = process.env.VERCEL === '1';
const skipCheck = process.env.SKIP_GAIOL_API_BASE_CHECK === '1';

if (onVercel && !base && !skipCheck) {
    console.error('');
    console.error('GAIOL: Missing GAIOL_API_PUBLIC_URL on Vercel.');
    console.error('  1. Vercel → Project → Settings → Environment Variables');
    console.error('     Add GAIOL_API_PUBLIC_URL = https://your-service.onrender.com (no trailing slash)');
    console.error('     Enable it for Production (and Preview if you use preview deploys).');
    console.error('  2. Redeploy (Deployments → ... → Redeploy). Env is read at build time.');
    console.error('  To allow deploy without API (not recommended): SKIP_GAIOL_API_BASE_CHECK=1');
    console.error('');
    process.exit(1);
}

const outPath = path.join(__dirname, '..', 'web', 'js', 'gaiol-api-config.js');
const contents =
    '// Generated by scripts/inject-gaiol-config.js\n' +
    `window.__GAIOL_API_BASE__ = ${JSON.stringify(base)};\n`;

fs.writeFileSync(outPath, contents, 'utf8');
console.log('inject-gaiol-config:', base ? `API base = ${base}` : 'API base = (empty, same-origin)');
