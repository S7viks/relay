#!/usr/bin/env node
/**
 * Writes dashboard/public/gaiol-api-config.js from GAIOL_API_PUBLIC_URL (or NEXT_PUBLIC_GAIOL_API_URL).
 * Optional: the React app uses VITE_API_BASE at build time (preferred). This file is only useful if
 * you inject a legacy script tag; the SPA does not load it by default.
 */
const fs = require('fs');
const path = require('path');

const raw = (process.env.GAIOL_API_PUBLIC_URL || process.env.NEXT_PUBLIC_GAIOL_API_URL || '').trim();
const base = raw.replace(/\/+$/, '');

const onVercel = process.env.VERCEL === '1';
const skipCheck = process.env.SKIP_GAIOL_API_BASE_CHECK === '1';

if (onVercel && !base && !skipCheck) {
    console.error('');
    console.error('GAIOL: Set VITE_API_BASE on Vercel for the React build (Go API origin, no trailing slash).');
    console.error('  Optional legacy: GAIOL_API_PUBLIC_URL for inject-gaiol-config.js output.');
    console.error('  To allow deploy without API (not recommended): SKIP_GAIOL_API_BASE_CHECK=1');
    console.error('');
    process.exit(1);
}

const outPath = path.join(__dirname, '..', 'dashboard', 'public', 'gaiol-api-config.js');
fs.mkdirSync(path.dirname(outPath), { recursive: true });
const contents =
    '// Generated by scripts/inject-gaiol-config.js\n' +
    `window.__GAIOL_API_BASE__ = ${JSON.stringify(base)};\n`;

fs.writeFileSync(outPath, contents, 'utf8');
console.log('inject-gaiol-config:', base ? `API base = ${base}` : 'API base = (empty, same-origin)');
